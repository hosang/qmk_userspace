// The upright display has a width of 5 characters. Each character has a width
// of 6 pixels. That gives 60 pixels for a row.
#define WPM_NUM_SAMPLES 30
void render_wpm(int x, int y) {
    static uint32_t anim_timer = 0;
    if (timer_elapsed32(anim_timer) < 2 * ANIM_FRAME_DURATION) {
        return;
    }
    anim_timer = timer_read32();
    uint8_t wpm = get_current_wpm();

    char buf[8];
    sprintf(buf, "  %3d", wpm);
    oled_set_cursor(x, y);
    oled_write(buf, false);

    static float avg_wpm = 0;
    avg_wpm = 0.9 * avg_wpm + 0.1 * wpm;
    sprintf(buf, "a %3d", (int)avg_wpm);
    oled_set_cursor(x, y + 1);
    oled_write(buf, false);

    static char graph[2][WPM_NUM_SAMPLES];
    memmove(graph[0], graph[0] + 1, WPM_NUM_SAMPLES - 1);
    memmove(graph[1], graph[1] + 1, WPM_NUM_SAMPLES - 1);
    uint8_t qwpm = get_current_wpm() / 10;
    if (qwpm < 8) {
        graph[0][WPM_NUM_SAMPLES - 1] = 1 << (7 - qwpm);
        graph[1][WPM_NUM_SAMPLES - 1] = 0;
    } else {
        graph[0][WPM_NUM_SAMPLES - 1] = 0;
        graph[1][WPM_NUM_SAMPLES - 1] = 1 << (14 - qwpm);
    }
    oled_set_cursor(x, y + 3);
    oled_write_raw(graph[1], WPM_NUM_SAMPLES);
    oled_set_cursor(x, y + 4);
    oled_write_raw(graph[0], WPM_NUM_SAMPLES);
}

bool oled_task_user() {
    if (is_keyboard_master()) {
        switch (get_highest_layer(layer_state)) {
            case 0:
                oled_write_P(PSTR("qwrty\n"), false);
                break;
            case 1:
                oled_write_P(PSTR("LOWR\n"), false);
                break;
            case 2:
                oled_write_P(PSTR("RAIS\n"), false);
                break;
            case 3:
                oled_write_P(PSTR("FUN\n"), false);
                break;
            default:
                oled_write_P(PSTR("undef\n"), false);
                break;
        }
        render_luna(0, 13);
    } else {
        render_wpm(0, 10);
    }
    return false;
}

bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    switch (keycode) {
        case OSM_SFT: case OSM(MOD_LSFT):
            is_barking = record->event.pressed;
            break;
        case OSM_CTL:
            is_sneaking = record->event.pressed;
            break;
        case KC_SPC:
            is_jumping = record->event.pressed;
            showed_jump &= !record->event.pressed;
            break;
    }
    return true;
}

